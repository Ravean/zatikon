package leo.server;

// imports

import java.util.Vector;


public class CastleArchive {

    /////////////////////////////////////////////////////////////////
    // Properties
    /////////////////////////////////////////////////////////////////
    private final String name;
    private final Vector<Short> units = new Vector<Short>();

    /////////////////////////////////////////////////////////////////
    // Constructor
    /////////////////////////////////////////////////////////////////
    public CastleArchive(String newName) {
        name = newName;
    }


    /////////////////////////////////////////////////////////////////
    // Get name
    /////////////////////////////////////////////////////////////////
    public String getName() {
        return name;
    }


    /////////////////////////////////////////////////////////////////
    // add a unit
    /////////////////////////////////////////////////////////////////
    public void add(short newUnit) {
        units.add(newUnit);
    }


    /////////////////////////////////////////////////////////////////
    // Get a unit
    /////////////////////////////////////////////////////////////////
    public short get(int unit) {
        return units.elementAt(unit).byteValue();
    }


    /////////////////////////////////////////////////////////////////
    // Length
    /////////////////////////////////////////////////////////////////
    public short size() {
        return (byte) units.size();
    }

}
